    public void refreshTableUser(){
        try{
        List<User> users = TaskFlow.getUsers();
        userModel.refreshTable(users);
        }
        catch (Exception error){
            System.out.println("Erro ao recarregar Tabela; Erro: " +error);
        }
    }
    
    public void searchTableUser(String info){
        try{
        List<User> users = TaskFlow.searchUser(info);
        userModel.refreshTable(users);
        }
        catch (Exception error){
            System.out.println("Erro ao recarregar Tabela; Erro: " +error);
        }
        
    }
    
    
