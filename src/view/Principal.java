    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int index = tbUsers.getSelectedRow();
        boolean res = false;
        if (index == -1){           
            this.errorPane("Selecione uma Linha para alterar!");
        } else
        if (JOptionPane.showConfirmDialog(this, "Deseja alterar o Usuário?", 
                "Alterar Usuário",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            User userClicked = this.userModel.returnUser(index);
            String id = String.valueOf(userClicked.getId());
            String fn = userClicked.getFirstName();
            String ln = userClicked.getLastName();
            String user = userClicked.getUsername();
            String email = userClicked.getEmail();
            
            String[] info = {id,fn,ln,user,email};
            
            try{
                res = TaskFlow.updateUser(info);
            }
            catch(Exception error){
                this.errorPane("" + error);
            }
        }
        if (res){
            this.refreshTableUser();
            this.sucessPane("Usuário Atualizado!");
        }           
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int index = tbUsers.getSelectedRow();
        boolean res = false;
        if (index == -1){           
            this.errorPane("Selecione uma Linha para alterar!");
        } else
        if (JOptionPane.showConfirmDialog(this, "Deseja Remover o Usuário?", 
                "Deletar Usuário",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            User userClicked = this.userModel.returnUser(index);
            int id = userClicked.getId();
            try{
                res = TaskFlow.deleteUser(id);
            }
            catch(Exception error){
                this.errorPane("" + error);
            }
        } 
        if(res){
            this.refreshTableUser();
            this.sucessPane("Usuário Deletado!");
        }          
    }//GEN-LAST:event_btnDeleteActionPerformed

