        String user = txtUser.getText();
        String password = String.valueOf(txtPassword.getPassword());
        boolean resultado = false;
        
        if (user.length() == 0 || password.length() == 0) {
            this.errorPane("Preencha todos os Campos!");
        } else{
            try{
                resultado = TaskFlow.autenticar(user,password);

            } catch(Exception error){
                this.errorPane("Erro: "+error);
            }
            if (resultado){
                this.setVisible(false);
                Principal tela2 = new Principal();
                tela2.setVisible(true);
                //trocar pag
            } else{
                this.errorPane("Credenciais inv√°lidas");
            }
        }
